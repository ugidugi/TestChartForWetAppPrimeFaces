class User {
    private int year;
    private int age;

    public User(int year, int age) {
        this.year = year;
        this.age = age;
    }
    
    
    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
    
    
}
